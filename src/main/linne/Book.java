package linne;

public class Book {
    private String name;
    private int isbn;
    private double price;
    private String author;


    public void setName(String name) {
        this.name = name;
    }

    public void setIsbn(int isbn) {
        this.isbn = isbn;
    }

    public void setPrice(double price) {
        this.price = price;
    }

    public void setAuthor(String author) {
        this.author = author;
    }
}
